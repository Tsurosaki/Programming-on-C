#include <stdio.h>

int main() {
    char str[81]; // Массив для хранения строки (до 80 символов + 1 для нуль-терминатора)

    // Вводим строку с консоли
    printf("Введите строку (до 80 символов): ");
    fgets(str, sizeof(str), stdin); // Используем fgets для безопасного ввода

    // Проходим по каждому символу строки
    for (int i = 0; str[i] != '\0'; i++) {
        // Меняем 'a' на 'A' и 'b' на 'B'
        if (str[i] == 'a') {
            str[i] = 'A';
        } else if (str[i] == 'b') {
            str[i] = 'B';
        }
    }

    // Выводим полученную строку
    printf("Измененная строка: %s\n", str);

    return 0;
}
